CREATE TABLE staging.jobs (
  staging_id SERIAL PRIMARY KEY,
  job_id TEXT,
  source_system VARCHAR(25),
  job_url VARCHAR(500),
  job_title VARCHAR(255),
  company_name VARCHAR(255),
  company_logo VARCHAR(500),
  job_industry TEXT[],
  job_type TEXT[],
  job_level VARCHAR(50),
  job_description TEXT,
  job_excerpt TEXT,
  is_partner BOOLEAN,
  is_paused BOOLEAN,
  location VARCHAR(255),
  latitude DECIMAL(11,8),
  longitude DECIMAL(11,8),
  city_category VARCHAR(50),
  state_category VARCHAR(50),
  address VARCHAR(255),
  postal_code VARCHAR(50),
  remote_type VARCHAR(50),
  workplace VARCHAR(50),
  candidate_contact_way VARCHAR(50),
  redirect_job_url VARCHAR(500),
  salary_min INT, 
  salary_max INT,
  salary_currency VARCHAR(3),
  perk_keys TEXT[],
  company_size VARCHAR(50), 
  company_type VARCHAR(50),
  tech_category VARCHAR(50),
  tech_stack TEXT[],
  filter_tags TEXT[],
  has_visa_sponsorship VARCHAR(50),
  language_requirements TEXT[],
  published_at TIMESTAMP,  
  last_update TIMESTAMP,
  ingested_ts TIMESTAMP,
  staging_ts TIMESTAMP
);
